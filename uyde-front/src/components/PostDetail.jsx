                    <li className="list-group-item">
                        <strong>–í–ª–∞–¥–µ–ª–µ—Ü:</strong> {post.owner?.full_name || `–ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å ID ${post.owner?.id || '–Ω–µ–∏–∑–≤–µ—Å—Ç–µ–Ω'}`}
                    </li>
